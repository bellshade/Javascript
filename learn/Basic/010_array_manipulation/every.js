const exampleArray = [60, 70, 80, 90];

console.log(exampleArray.every((value) => value > 50)); // output: true

// every() akan menjalankan fungsi pada setiap element dimana pada setiap iterasinya akan mengecek element apakah memenuhi kondisi pada fungsi yang di buat.
// hasil dari every() yaitu true atau false
